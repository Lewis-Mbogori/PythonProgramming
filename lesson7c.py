# While Loop
# We use while loop to get infinite loops
# print 10 numbers
# start, stop(limit), step(interval)

# Start
count = 0

# stop
while count < 11:
    print(count)
    # step
    count = count + 1

print("===============")
# print numbers from 10 to 4

